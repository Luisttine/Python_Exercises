class Calculator:

    def sum(self, a, b): print(f'\nA soma de {a} por {b} é: {a+b}')
    
    def sub(self, a, b): print(f'\nA subtração de {a} por {b} é: {a-b}')
    
    def prod(self, a, b): print(f'\nO produto de {a} por {b} é: {a*b}')
    
    def division(self, a, b): print(f'\nA divisão de {a} por {b} é: {a/b}')